  ARENAEVT2: `${TESTRUNID}_arenaevent_2`,  // arena events under EVENT2 (productref → P_INST)
  PTDS_INIT: `${TESTRUNID}_ptds_init`,     // productToDeliverySequence for P_INST (initiate Delivery Set list)
