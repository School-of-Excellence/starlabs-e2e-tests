  QUEUE1: `${TESTRUNID}_queue_1`,          // queue generation (selectable; queueenddate future)
  QTOK1: `${TESTRUNID}_qtok_1`,            // queue_token (Active, currentstage 'Stage A')
  QTOK2: `${TESTRUNID}_qtok_2`,            // queue_token (Active, currentstage 'Stage A')
