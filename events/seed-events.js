  ETICKET_P0: `${TESTRUNID}_eticket_p0`,   // arena e-ticket (active:true) for p0 on EVENT1
  ETLOG_DUP: `${TESTRUNID}_etlog_dup`,     // a pre-existing log row → EVT-08 duplicate-guard uniqueid

